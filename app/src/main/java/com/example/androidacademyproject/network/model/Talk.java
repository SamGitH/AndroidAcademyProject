package com.example.androidacademyproject.network.model;

public class Talk {

    String title;
    String description;
    String room;
    String track;
    String speaker;
    String time;

    public String getTitle() {
        return title;
    }

    public String getDescription() {
        return description;
    }

    public String getRoom() {
        return room;
    }

    public String getTrack() {
        return track;
    }

    public String getSpeaker() {
        return speaker;
    }

    public String getTime() {
        return time;
    }
}
